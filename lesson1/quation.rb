print "Введите первое значение "
a = gets.to_f

print "Введите второе значение "
b = gets.to_f

print "Введите третье значение "
c = gets.to_f

d = (b**2) - (4 * a *c)

if d < 0
  puts "Корни выражения отсутствуют"

elsif d == 0
  x = -b / (2.0 * a)
  puts "Дискриминант равен #{d}, корень равен #{x}"

else
  sqr = Math.sqrt(d)
  x1 = (-b + sqr) / (2.0 * a)
  x2 = (-b - sqr) / (2.0 * a)
  puts "Дискриминант равен #{d}, корни равны #{x1} и #{x2}"
end
		
		